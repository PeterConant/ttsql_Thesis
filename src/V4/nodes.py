def gen_llm_call(state: State):
    """LLM decides whether to call a tool or not"""
    llm = state['llm']
    structured_sql_gen_llm = llm.with_structured_output(Generated_SQL,
                    strict=True,
                    include_raw=True
                )
    table_schemas = get_table_schemas_and_samples(state['tables'])
    system_message_content = dedent(
        """
        **Task Description:** 
        You are an SQL database expert tasked with writing a SQL query. A previous method has analyzed the original question and evidence, and retrieved the most relevant tables from the database. Your role is to analyze the original question based on the provided evidence and database schema, and then provide a SQL query. 

        **Procedure:** 
        1. Review Database Schema: 
        - Examine the table creation statements to understand the database structure. 
        2. Analyze Query Requirements: 
        - Original Question: Consider what information the query is supposed to retrieve. 
        - Hint: Use the provided evidence to understand the relationships and conditions relevant to the query. 
        3. Generate the Query: 
        - Generate the SQL query to address the original question, ensuring it correctly fetches the requested data according to the database schema and query requirements.  

        **Output Format:**  
        Present your query as a single line of SQL code. Ensure there are no line breaks within the query.
        
        **TABLE SCHEMAS**
        {table_schemas}
        """).format(table_schemas=table_schemas)   
    with get_usage_metadata_callback() as cb:
        response = structured_sql_gen_llm.invoke([SystemMessage(content=system_message_content)] + [state["messages"][0]])
        metadata = cb.usage_metadata[state['llm_path']]
        state["input_tokens"].append(metadata["input_tokens"])
        state["output_tokens"].append(metadata["output_tokens"])
    
    # Returns parsed as None when tool is called
    if response['parsed']:
        return {"messages": response['raw'], "sql": response['parsed'].sql, "input_tokens":state["input_tokens"], "output_tokens":state["output_tokens"],"retries": state["retries"]}
    # Tool call is in raw
    else:
        return {"messages": response['raw']}
    
