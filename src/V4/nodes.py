def tool_node(state: State):
    """Performs the SQL statement execution tool call"""
    
    tools = state['tools']
    tools_by_name = {tool.name: tool for tool in tools}

    result = []
    table_list = state['tables']
    for tool_call in state["messages"][-1].tool_calls:
        try:
            tool = tools_by_name[tool_call["name"]]
            args = tool_call["args"].copy()
            # if "table_list" in args:
            #     table_list.append(args["table_list"])
            observation = tool.invoke(tool_call["args"])
            result.append(ToolMessage(content=observation, tool_call_id=tool_call["id"]))
        except Exception as e:
            result.append(ToolMessage(content=str(e), tool_call_id=tool_call["id"]))
    return {"messages":result, "tables":table_list}


